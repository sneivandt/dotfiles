/// Header written at the top of the generated `~/.apm/apm.yml`.
pub(super) const GENERATED_HEADER: &str =
    "# Generated by dotfiles from ~/.apm/config/*.yml fragments.\n# Manual edits will be lost.\n";
pub(super) const APM_UP_TO_DATE_MARKER: &str = "all dependencies are up-to-date";

fn skip_with_warning(ctx: &Context, reason: impl Into<String>) -> TaskResult {
    let reason = reason.into();
    ctx.log.warn(&format!("skipping: {reason}"));
    TaskResult::Skipped(reason)
}

