//! Task: install AI plugin manifests via Microsoft APM.
//!
//! See <https://github.com/microsoft/apm>.  The dotfiles repo ships an
//! `apm/config/base.yml` under `symlinks/` which is linked to
//! `~/.apm/config/base.yml` by the
//! [`InstallSymlinks`](crate::tasks::files::symlinks::InstallSymlinks) task.  Overlay
//! repositories may symlink additional fragments into `~/.apm/config/*.yml`
//! (for example `~/.apm/config/work.yml`).
//!
//! Because `apm install -g --target copilot,vscode,copilot-app` always reads
//! the manifest at `~/.apm/apm.yml` regardless of cwd, this task merges every
//! `~/.apm/config/*.yml` fragment into a single generated `~/.apm/apm.yml`.
//! It then invokes
//! `apm install -g --target copilot,vscode,copilot-app` whenever install state
//! must be materialized, including ordinary up-to-date runs so local symlinked
//! plugin edits are redeployed to their real runtime locations.
//! Idempotency is provided by APM itself via its lockfile; the merged file is
//! regenerated deterministically every run.
//!
//! The experimental `copilot-app` target integrates hand-authored Copilot App
//! workflow prompts (`.prompt.md` files carrying workflow frontmatter) into
//! `~/.copilot`.  It is gated behind apm's experimental flag, persisted in the
//! machine-local `~/.apm/config.json`, so this task first runs the idempotent
//! `apm experimental enable copilot-app` (see `ensure_copilot_app_enabled`) to
//! keep fresh machines reproducible.  That step is best-effort: an older apm
//! without the subcommand simply logs a warning and the non-experimental
//! targets still install.  Verified against apm 0.16.0, where only `.prompt.md`
//! fragments with workflow frontmatter are encoded (under the `apm--` id
//! namespace); other primitives are left to the `copilot`/`vscode` targets.

