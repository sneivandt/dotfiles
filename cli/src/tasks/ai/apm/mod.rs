//! Task: install AI plugin manifests via Microsoft APM.
//!
//! See <https://github.com/microsoft/apm>.  The dotfiles repo ships an
//! `apm/config/base.yml` under `symlinks/` which is linked to
//! `~/.apm/config/base.yml` by the
//! [`InstallSymlinks`](crate::tasks::files::symlinks::InstallSymlinks) task.  Overlay
//! repositories may symlink additional fragments into `~/.apm/config/*.yml`
//! (for example `~/.apm/config/work.yml`).
//!
//! Because `apm install -g --target ...` always reads the manifest at
//! `~/.apm/apm.yml` regardless of cwd, this task merges every
//! `~/.apm/config/*.yml` fragment into a single generated `~/.apm/apm.yml`.
//! It then invokes `apm install -g --target copilot` plus `copilot-app` when the
//! Copilot App database is present, whenever install state must be materialized,
//! including ordinary up-to-date runs so local symlinked plugin edits are
//! redeployed to their real runtime locations.
//! Idempotency is provided by APM itself via its lockfile; the merged file is
//! regenerated deterministically every run.
//!
//! Advancing locked dependency refs (`apm outdated` followed by `apm update`)
//! is **not** part of this task.  It is handled separately
//! by [`UpdateApmPackages`], which runs in [`TaskPhase::Update`] and is only
//! scheduled by the `update` command (when `ctx.advance_versions` is set).
//! The `install` command therefore converges to the declared/locked manifest
//! without moving any locked ref forward, and shows no `Updating dependencies`
//! phase.  See [`advance_apm_dependencies`].
//!
//! The experimental `copilot-app` target integrates hand-authored Copilot App
//! workflow prompts (`.prompt.md` files carrying workflow frontmatter) into
//! `~/.copilot`.  It is gated behind apm's experimental flag, persisted in the
//! machine-local `~/.apm/config.json`, so this task first runs the idempotent
//! `apm experimental enable copilot-app` (see `ensure_copilot_app_enabled`) to
//! keep fresh machines reproducible.  That step is best-effort: an older apm
//! without the subcommand simply logs a warning and the non-experimental
//! targets still install.
//!
//! The `copilot-app` target only meaningfully encodes `.prompt.md` fragments
//! with workflow frontmatter (under the `apm--` id namespace).  In apm 0.16.x,
//! however, it also walks other deployed primitives and refuses to
//! lockfile-encode any whose id is outside that namespace -- notably the
//! `.agent.md` agent files shipped by third-party packages -- which makes
//! `apm install` exit non-zero even though the `copilot`/`vscode` targets
//! deployed those agents correctly.  Because that failure is a benign,
//! upstream-only limitation of the experimental target,
//! [`run_apm_install`] tolerates it when (and only when) every reported error
//! is that specific encoding failure; any other error still fails the task.
//! See [`tolerable_workflow_encode_failures`].

